const video = document.getElementById('myVideo');
const playButton = document.getElementById('playButton');

playButton.addEventListener('click', function() {
    if (video.paused) {
        video.play();
        playButton.style.display = 'none'; // Hide the play button when the video plays
    } else {
        video.pause();
        playButton.style.display = 'block'; // Show the play button when the video is paused
    }
});

// Show the play button when the video is not playing
video.addEventListener('pause', () => {
    playButton.style.display = 'block';
});

// Hide the play button when the video is playing
video.addEventListener('play', () => {
    playButton.style.display = 'none';
});

document.querySelector('.php-email-form').addEventListener('submit', function (e) {
    e.preventDefault(); // Prevent the default form submission

    const form = e.target;
    const formData = new FormData(form);
    const loading = document.querySelector('.loading');
    const errorMessage = document.querySelector('.error-message');
    const sentMessage = document.querySelector('.sent-message');

    loading.style.display = 'block';
    errorMessage.style.display = 'none';
    sentMessage.style.display = 'none';

    fetch(form.action, {
        method: form.method,
        body: formData
    })
    .then(response => response.text())
    .then(data => {
        loading.style.display = 'none';
        if (data.startsWith("success")) {
            sentMessage.style.display = 'block';
            form.reset(); // Reset the form after submission
        } else {
            errorMessage.style.display = 'block';
            errorMessage.innerText = data.replace("error: ", "");
        }
    })
    .catch(() => {
        loading.style.display = 'none';
        errorMessage.style.display = 'block';
        errorMessage.innerText = "An error occurred. Please try again.";
    });
});
